#include <gtest/gtest.h>
#include "aes.h"
#include "aes_ecb.h"

using namespace creep;

static void compare(uint8_t *out, const uint8_t *exp, size_t len)
{
    ASSERT_TRUE(out);
    ASSERT_TRUE(exp);

    for (size_t i = 0; i < len; ++i) {
        EXPECT_EQ(out[i], exp[i]) << "At index " << i;
    }
}

TEST(Aes128, Encrypt)
{
    uint8_t out[16] = {};
    const uint8_t in[16] = { 0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff };
    const uint8_t key[16] = { 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f };
    const uint8_t exp[16] = { 0x69, 0xc4, 0xe0, 0xd8, 0x6a, 0x7b, 0x04, 0x30, 0xd8, 0xcd, 0xb7, 0x80, 0x70, 0xb4, 0xc5, 0x5a };

    AES ctx;
    ctx.init(key);
    ctx.encrypt(in, out);
    compare(out, exp, 16);
}

TEST(Aes128, Decrypt)
{
    uint8_t out[16] = {};
    const uint8_t in[16] = { 0x69, 0xc4, 0xe0, 0xd8, 0x6a, 0x7b, 0x04, 0x30, 0xd8, 0xcd, 0xb7, 0x80, 0x70, 0xb4, 0xc5, 0x5a };
    const uint8_t key[16] = { 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f };
    const uint8_t exp[16] = { 0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff };

    AES ctx;
    ctx.init(key);
    ctx.decrypt(in, out);
    compare(out, exp, 16);
}

